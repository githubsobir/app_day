  String name;
  List<Root> root;

  Model({
    required this.name,
    required this.root,
  });

  factory Model.fromJson(Map<String, dynamic> json) => Model(
    name: json["name"],
    root: List<Root>.from(json["root"].map((x) => Root.fromJson(x))),
  );

  Map<String, dynamic> toJson() => {
    "name": name,
    "root": List<dynamic>.from(root.map((x) => x.toJson())),
  };
}

